#FATIAMENTO DE STRINGS

frase = 'Curso em Video Python'
print(frase[0]) #primeira letra

#fatiamento
print(frase [0:5])
print(frase[1::3]) #começa no 1, vai até o 7, pulando de 3 em 3
print(frase[-1: -10: -1])   #começa no ultimo, vai até o 10, de tras para frente
#funçao len  retorna a quantidade de caracteres
print(len(frase))